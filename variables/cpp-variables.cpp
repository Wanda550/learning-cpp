#include <iostream> 

using namespace std;

int main() {
  int a = 10; // Integer variable
  double b = 20.5; // Double variable
  char c = 'A'; // Character variable
  string d = "Hello, World!"; // String variable
  bool e = true; // Boolean variable

  cout << "Integer: " << a << endl;
  cout << "Double: " << b << endl;
  cout << "Character: " << c << endl;
  cout << "String: " << d << endl;
  cout << "Boolean: " << e << endl;

  return 0;
};